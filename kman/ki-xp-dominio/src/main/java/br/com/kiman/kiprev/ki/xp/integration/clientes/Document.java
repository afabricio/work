package br.com.kiman.kiprev.ki.xp.integration.clientes;

public class Document {

	private String documentNumber;
	private String dispatchingOrgan;
	private String dispatchingState;
	private String emissionDate;
	private String documentType;
	private String country;
	private String securityCNHCode;
	private String nacionality;
	private String naturality;
	private String birthState;

	public String getDocumentNumber() {
		return documentNumber;
	}

	public void setDocumentNumber(String documentNumber) {
		this.documentNumber = documentNumber;
	}

	public String getDispatchingOrgan() {
		return dispatchingOrgan;
	}

	public void setDispatchingOrgan(String dispatchingOrgan) {
		this.dispatchingOrgan = dispatchingOrgan;
	}

	public String getDispatchingState() {
		return dispatchingState;
	}

	public void setDispatchingState(String dispatchingState) {
		this.dispatchingState = dispatchingState;
	}

	public String getEmissionDate() {
		return emissionDate;
	}

	public void setEmissionDate(String emissionDate) {
		this.emissionDate = emissionDate;
	}

	public String getDocumentType() {
		return documentType;
	}

	public void setDocumentType(String documentType) {
		this.documentType = documentType;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getSecurityCNHCode() {
		return securityCNHCode;
	}

	public void setSecurityCNHCode(String securityCNHCode) {
		this.securityCNHCode = securityCNHCode;
	}

	public String getNacionality() {
		return nacionality;
	}

	public void setNacionality(String nacionality) {
		this.nacionality = nacionality;
	}

	public String getNaturality() {
		return naturality;
	}

	public void setNaturality(String naturality) {
		this.naturality = naturality;
	}

	public String getBirthState() {
		return birthState;
	}

	public void setBirthState(String birthState) {
		this.birthState = birthState;
	}

}
