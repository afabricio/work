package br.com.kiman.kiprev.ki.xp.integration.clientes;

public class TaxAddress {

	private String street;
	private String number;
	private String complementation;
	private String neighborhood;
	private String city;
	private String zipCode;
	private String federalUnit;
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getComplementation() {
		return complementation;
	}
	public void setComplementation(String complementation) {
		this.complementation = complementation;
	}
	public String getNeighborhood() {
		return neighborhood;
	}
	public void setNeighborhood(String neighborhood) {
		this.neighborhood = neighborhood;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getZipCode() {
		return zipCode;
	}
	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}
	public String getFederalUnit() {
		return federalUnit;
	}
	public void setFederalUnit(String federalUnit) {
		this.federalUnit = federalUnit;
	}
	
	
	

}
