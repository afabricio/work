package br.com.kiman.kiprev.ki.xp.integration.itau;

public class Moeda {
	private  String sigla_moeda;
	private  String quantidade_moeda;
	private  String cotacao_moeda;
	
	public String getSigla_moeda() {
		return sigla_moeda;
	}
	public void setSigla_moeda(String sigla_moeda) {
		this.sigla_moeda = sigla_moeda;
	}
	public String getQuantidade_moeda() {
		return quantidade_moeda;
	}
	public void setQuantidade_moeda(String quantidade_moeda) {
		this.quantidade_moeda = quantidade_moeda;
	}
	public String getCotacao_moeda() {
		return cotacao_moeda;
	}
	public void setCotacao_moeda(String cotacao_moeda) {
		this.cotacao_moeda = cotacao_moeda;
	}




}