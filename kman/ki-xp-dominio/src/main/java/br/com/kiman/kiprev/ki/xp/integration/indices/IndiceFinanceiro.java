package br.com.kiman.kiprev.ki.xp.integration.indices;

public class IndiceFinanceiro {
	
	private String id;
	private String name;
	private String displayName;
	private String comDinheiroName;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDisplayName() {
		return displayName;
	}
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	public String getComDinheiroName() {
		return comDinheiroName;
	}
	public void setComDinheiroName(String comDinheiroName) {
		this.comDinheiroName = comDinheiroName;
	}

}
